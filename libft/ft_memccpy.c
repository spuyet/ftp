#include "libft.h"

void	*ft_memccpy(void *s1, const void *s2, int c, size_t n)
{
	size_t				i;
	unsigned char		*str1;
	unsigned char		*str2;

	i = 0;
	str1 = (unsigned char *)s1;
	str2 = (unsigned char *)s2;
	while (i < n)
	{
		str1[i] = str2[i];
		if (str2[i] == c)
			return ((void *)&(str1[i + 1]));
		i++;
	}
	return (0);
}
